
class ShapeCoordinate
{
    private int mX;
    private int mY;

    public ShapeCoordinate(int pX, int pY)
    {
	mX = pX;
	mY = pY;
    }

    public void setX(int pX)
    {
	mX = pX;
    }
    
    public void setY(int pY)
    {
	mY = pY;
    }

    public int getX()
    {
	return mX;
    }

    public int getY()
    {
	return mY;
    }
}
